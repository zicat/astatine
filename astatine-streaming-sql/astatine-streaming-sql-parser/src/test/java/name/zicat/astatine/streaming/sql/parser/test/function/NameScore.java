package name.zicat.astatine.streaming.sql.parser.test.function;

import java.util.Objects;

/** NameScore. */
public class NameScore {
    private String name;
    private int score;

    public NameScore() {}

    public NameScore(String name, int score) {
        this.name = name;
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    @Override
    public String toString() {
        return "{" + name + "," + score + "}";
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        final var nameScore = (NameScore) o;
        return score == nameScore.score && Objects.equals(name, nameScore.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, score);
    }
}
